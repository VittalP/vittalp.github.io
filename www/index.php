			<h3>Brief Bio</h3>
			
			<p>Vittal Premachandran is a Postdoctoral Research Fellow at the University of California, Los Angeles. He obtained the B.E. degree in Computer Science from Visveswaraya Technological University, India, in 2009, graduating with a First Class with Distinction. He received his Ph.D degree, also in Computer Science, from Nanyang Technological University, Singapore, in 2014. He has previously worked as a Postdoctoral Research Fellow at the National University of Singapore.</p>

			<p>His research interests span computer vision and machine learning, with a focus on semantic analysis of scenes. His work has received the Best Student Paper award at the IEEE International Conference on Image Processing (ICIP) in 2013.</p>
			
